							<h2 class="entry-title">We are pioneering a new model for web journalism.</h2>
						</header>
								
						<div class="entry-content">

							<p>As the Internet blossoms newspapers are withering, and the <a href="/approach-to-community-news" title="Approach to Community News">reliable news coverage and information</a> that communities need for their civic health are withering with them. New models for community journalism that can thrive in the digital future are an urgent need of our democracy, and the nonprofit Banyan Project is responding. Its mission is to help seed independent community news cooperatives, then support them with mentorship and educational and administrative tools.</p>
				
							<p>The multifaceted model that Banyan advocates and supports is built on the sturdy foundation of <a href="/co-op-ownership" title="Co-op Ownership">consumer co-ops</a>, with reader-members electing the boards of local news co-ops the way shoppers do in food co-ops and depositors do in credit unions. The news co-op model, as Banyan has developed it, is easily replicable from community to community, the way food co-ops and credit unions replicated from coast to coast.</p>
							
							<p>Each independent news co-op’s voting members will be hundreds if not thousands of local readers. The co-ops will be led professionally and governed democratically through one-member/one-vote election of directors, as are co-ops of all kinds. Their <a href="/sources-of-revenue" title="Sources of Revenue">revenue structure</a> is designed to make them thrive even as newspapers fade. And their journalism will be free for all to read so they can serve the broad public of the less-than-affluent everyday citizens we call <a href="/banyan-public" title="Banyan Public">the Banyan public</a>, not just the upscale people newspapers tend to cultivate. The need for news co-ops is greatest in underserved communities.</p>
							
							<p>Banyan educational materials will include a comprehensive guide for creating a news co-op, templates for business planning, and plans for enrolling founding members, all supported by Banyan staff guidance. Ongoing staff support will include problem solving and online forums.</p>
							
							<p>Banyan will also provide affiliated sites with <a href="http://banyanproject.coop/software-platform" title="software Platform">software tailored for community news co-ops</a>. Banyan’s staff will help sites make the most of distinctive software features designed to amplify their journalism’s impact:</p>
							
							<ul>
								<li>Digital tools designed to make it comfortable and gratifying for readers to collaborate with the editors will attract voluntary energy that can significantly expand the <a href="/approach-to-community-news" title="Approach to Community News">scope of coverage</a> possible with a small staff. This is the same kind of voluntary energy the Web unleashes to fuel Wikipedia and open source software, only on a community scale.<br>
								</li>
								<li>The software will also include <a href="/civic-engagement" title="Civic Engagement">civic engagement tools</a> that invite readers to join with others and work for constructive community change, making it easy for communities to find and express their voices—and strengthen their civic infrastructure.</li>
							</ul>
							
							<p>In short, independent news co-ops that follow this model will be democracy-strengthening community institutions—of, by and for their communities.</p>

